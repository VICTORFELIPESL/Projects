    private boolean validarCampos() {
        
        String login = campoLogin.getText();
        String senha = campoSenha.getText();
       

        // Verificar se os campos obrigatórios estão vazios
        if (login.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos obrigatórios.", "Erro", JOptionPane.ERROR_MESSAGE);
            return false; // Retorna falso se a validação falhar
        }

        return true; // Retorna verdadeiro se a validação passar
    }
