    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (validarCampos()) {
         String login = campoLogin.getText();
         String senha = campoSenha.getText();

        // Consulta SQL para verificar as credenciais do usuário
        String sql = "SELECT id, nome, login, senha, email FROM usuario WHERE login = ? AND senha = ?";
        PreparedStatement ps;
        ResultSet rs;

    try {
        ps = ConexaoJDBC.getConexao().prepareStatement(sql);
        ps.setString(1, login);
        ps.setString(2, senha);
        rs = ps.executeQuery();

        if (rs.next()) {
            // Se a consulta retornar algum resultado, o login foi bem-sucedido
            JOptionPane.showMessageDialog(this, "Acesso Autorizado", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Caso contrário, o login falhou
            JOptionPane.showMessageDialog(this, "Acesso Negado", "Erro", JOptionPane.ERROR_MESSAGE);
        }

        // Limpar os campos de login e senha após a tentativa de login
        campoLogin.setText("");
        campoSenha.setText("");
    } catch (SQLException e) {
        e.printStackTrace();
    }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

